export interface FoodItem {
  name: string;
  region: string;
  category: string;
  kcalPerGram: number;
}

export const parseFoodDatabase = (): FoodItem[] => {
  const lines = RAW_FOOD_DATA.trim().split('\n');
  const dataLines = lines.slice(1); // Skip header
  
  return dataLines.map(line => {
    const parts = line.split(',');
    if (parts.length < 4) return null;
    return {
      name: parts[0].trim(),
      region: parts[1].trim(),
      category: parts[2].trim(),
      kcalPerGram: parseFloat(parts[3].trim()) || 0,
    };
  }).filter((item): item is FoodItem => item !== null);
};

export const searchFoods = (query: string): FoodItem[] => {
  const foods = parseFoodDatabase();
  const searchTerm = query.toLowerCase().trim();
  
  if (!searchTerm) return [];
  
  return foods.filter(food => 
    food.name.toLowerCase().includes(searchTerm) ||
    food.category.toLowerCase().includes(searchTerm) ||
    food.region.toLowerCase().includes(searchTerm)
  ).slice(0, 20); // Limit to 20 results
};

export const getFoodDatabasePrompt = (): string => {
  const lines = RAW_FOOD_DATA.trim().split('\n');
  const dataLines = lines.slice(1);
  
  return dataLines.map(line => {
    const parts = line.split(',');
    if (parts.length < 4) return '';
    const name = parts[0];
    const region = parts[1];
    const kcal = parts[3];
    return `- ${name} (${region}): ${kcal} kcal/g`;
  }).join('\n');
};
